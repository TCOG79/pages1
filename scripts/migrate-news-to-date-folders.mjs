#!/usr/bin/env node
/**
 * Move flat news markdown files into YYYY/MM/DD/ folders by publishedAt (Asia/Bangkok)
 * and regenerate src/config/news-redirects.ts for legacy /news/{slug}/ URLs.
 *
 * Usage: node scripts/migrate-news-to-date-folders.mjs
 */

import { mkdir, readFile, readdir, rename, writeFile } from 'node:fs/promises';
import { basename, dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const NEWS_DIR = join(ROOT, 'src', 'content', 'news');
const REDIRECTS_FILE = join(ROOT, 'src', 'config', 'news-redirects.ts');

const calendarDateFormatter = new Intl.DateTimeFormat('en-CA', {
	timeZone: 'Asia/Bangkok',
	year: 'numeric',
	month: '2-digit',
	day: '2-digit',
});

function getCalendarDateKey(date) {
	return calendarDateFormatter.format(date);
}

function parsePublishedAt(content, filename) {
	const match = content.match(/^publishedAt:\s*(\S+)/m);
	if (!match) {
		throw new Error(`Missing publishedAt in ${filename}`);
	}

	const date = new Date(match[1]);
	if (Number.isNaN(date.getTime())) {
		throw new Error(`Invalid publishedAt "${match[1]}" in ${filename}`);
	}

	return date;
}

function getTargetRelativePath(publishedAt, slug) {
	const [year, month, day] = getCalendarDateKey(publishedAt).split('-');
	return join(year, month, day, `${slug}.md`);
}

async function collectFlatMarkdownFiles(dir) {
	const entries = await readdir(dir, { withFileTypes: true });
	return entries
		.filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
		.map((entry) => entry.name);
}

async function collectAllMarkdownFiles(dir, prefix = '') {
	const entries = await readdir(dir, { withFileTypes: true });
	const files = [];

	for (const entry of entries) {
		const relativePath = prefix ? `${prefix}/${entry.name}` : entry.name;
		if (entry.isDirectory()) {
			files.push(...(await collectAllMarkdownFiles(join(dir, entry.name), relativePath)));
		} else if (entry.isFile() && entry.name.endsWith('.md')) {
			files.push(relativePath.replace(/\\/g, '/'));
		}
	}

	return files;
}

function buildRedirectMap(allFiles) {
	const redirects = {};

	for (const file of allFiles) {
		const normalized = file.replace(/\\/g, '/');
		const slug = basename(normalized, '.md');
		const datedMatch = normalized.match(/^(\d{4}\/\d{2}\/\d{2})\/(.+)\.md$/);
		if (!datedMatch) continue;

		const newUrlPath = `/news/${datedMatch[1]}/${datedMatch[2]}/`;
		redirects[`/news/${slug}`] = newUrlPath;
	}

	return redirects;
}

async function writeRedirectsFile(redirects) {
	const redirectEntries = Object.entries(redirects)
		.sort(([a], [b]) => a.localeCompare(b))
		.map(([from, to]) => `\t'${from}': '${to}',`)
		.join('\n');

	const redirectsSource = `/** Auto-generated by scripts/migrate-news-to-date-folders.mjs — do not edit manually */
export const newsRedirects: Record<string, string> = {
${redirectEntries}
};
`;

	await writeFile(REDIRECTS_FILE, redirectsSource, 'utf8');
}

async function migrate() {
	const flatFiles = await collectFlatMarkdownFiles(NEWS_DIR);
	const moved = [];
	const skipped = [];

	for (const file of flatFiles) {
		const sourcePath = join(NEWS_DIR, file);
		const slug = basename(file, '.md');
		const content = await readFile(sourcePath, 'utf8');
		const publishedAt = parsePublishedAt(content, file);
		const targetRelative = getTargetRelativePath(publishedAt, slug);
		const targetPath = join(NEWS_DIR, targetRelative);

		if (sourcePath === targetPath) {
			skipped.push(file);
			continue;
		}

		await mkdir(dirname(targetPath), { recursive: true });

		try {
			await rename(sourcePath, targetPath);
		} catch (error) {
			if (error && typeof error === 'object' && error.code === 'EEXIST') {
				console.warn(`Target exists, skipping: ${targetRelative}`);
				skipped.push(file);
				continue;
			}
			throw error;
		}

		moved.push({ from: file, to: relative(NEWS_DIR, targetPath).replace(/\\/g, '/') });
	}

	const allFiles = await collectAllMarkdownFiles(NEWS_DIR);
	const redirects = buildRedirectMap(allFiles);
	await writeRedirectsFile(redirects);

	console.log(`Moved ${moved.length} file(s), skipped ${skipped.length}.`);
	for (const item of moved) {
		console.log(`  ${item.from} → ${item.to}`);
	}
	console.log(`Wrote ${Object.keys(redirects).length} redirect(s) to ${relative(ROOT, REDIRECTS_FILE)}`);
}

migrate().catch((error) => {
	console.error(error);
	process.exit(1);
});
